export function createReviewMarkdown(review, profile, manifestRows = []) {
  const a=review.analysisSummary,q=review.qualificationSummary,m=review.modelSummary,s=review.solverSummary;
  const lines=[
    '# LFEA Engineering Review','',
    '## Analysis Identity','',
    `- Review identity: ${md(review.reviewIdentity)}`,
    `- Review semantic hash: ${md(review.semanticHash)}`,
    `- Model identity: ${md(a.modelIdentity)}`,
    `- Model semantic hash: ${md(a.modelHash)}`,
    `- Result schema: ${md(s.resultSchema)}`,
    `- Result semantic hash: ${md(a.resultHash)}`,
    `- Load case: ${md(a.loadCaseIdentity)}`,'',
    '## Qualification Status','',
    `- Review status: ${md(review.status)}`,
    `- Qualification rows: ${q.rows.length}`,
    `- PASS: ${q.counts.PASS}; NOT SUPPLIED: ${q.counts.NOT_SUPPLIED}; NOT APPLICABLE: ${q.counts.NOT_APPLICABLE}`,
    '- This review status is evidence-chain qualification only; it is not an engineering acceptance or code-compliance decision.','',
    '## Model Summary','',
    `- Formulation: ${md(m.formulation)}`,
    `- Units: length=${md(a.units.length)}, force=${md(a.units.force)}, stress=${md(a.units.stress)}`,
    `- Nodes: ${m.nodeCount}; elements: ${m.elementCount}; T3: ${m.t3Count}; Q4: ${m.q4Count}; materials: ${m.materialCount}`,
    `- Deformation scale: ${format(profile.deformationScale,profile.coordinateDisplayPrecision)} (${review.geometryReview.authority})`,'',
    '## Solver Summary','',
    `- Backend: ${md(s.backendIdentity)}`,
    `- Termination: ${md(s.terminationStatus)}`,
    `- Free residual infinity norm: ${format(s.freeResidualInfinity,profile.forceDisplayPrecision)}`,
    `- Complete residual infinity norm: ${format(s.completeResidualInfinity,profile.forceDisplayPrecision)}`,
    `- Strain energy: ${format(s.strainEnergy,profile.energyDisplayPrecision)}`,
    ...(s.trueResidualL2===null?[]:[`- Final true residual L2: ${format(s.trueResidualL2,profile.forceDisplayPrecision)}`,`- Iterations: ${s.iterationCount}`]),'',
    '## Loads and Constraints','',
    `- Nodal forces: ${review.loadReview.counts.nodalForce}`,
    `- Edge tractions: ${review.loadReview.counts.edgeTraction}`,
    `- Edge pressures: ${review.loadReview.counts.edgePressure}`,
    `- Applied totals: FX=${format(review.loadReview.totals.fx,profile.forceDisplayPrecision)}, FY=${format(review.loadReview.totals.fy,profile.forceDisplayPrecision)}, MZ=${format(review.loadReview.totals.mz,profile.forceDisplayPrecision)}`,
    `- Constrained nodes: ${review.constraintReview.constrainedNodeCount}; constrained DOFs: ${review.constraintReview.constrainedDofCount}; nonzero prescribed DOFs: ${review.constraintReview.prescribedNonzeroDofCount}`,'',
    '## Displacement Summary','',
    governingLine('Maximum displacement magnitude',review.displacementReview.governing.maximumMagnitude,profile.displacementDisplayPrecision),
    governingLine('Maximum absolute UX',review.displacementReview.governing.maximumAbsoluteUx,profile.displacementDisplayPrecision),
    governingLine('Maximum absolute UY',review.displacementReview.governing.maximumAbsoluteUy,profile.displacementDisplayPrecision),'',
    '## Reaction and Equilibrium Summary','',
    `- Reaction totals: FX=${format(review.reactionReview.totals.fx,profile.forceDisplayPrecision)}, FY=${format(review.reactionReview.totals.fy,profile.forceDisplayPrecision)}, MZ=${format(review.reactionReview.totals.mz,profile.forceDisplayPrecision)}`,
    `- Force-balance residual: FX=${format(review.reactionReview.forceBalanceResidual.fx,profile.forceDisplayPrecision)}, FY=${format(review.reactionReview.forceBalanceResidual.fy,profile.forceDisplayPrecision)}`,
    `- Moment-balance residual: ${format(review.reactionReview.momentBalanceResidual,profile.forceDisplayPrecision)}`,'',
    '## Raw Stress Summary','',
    `- Authority: ${review.rawStressReview.authority}`,
    stressLine('Maximum von Mises',review.rawStressReview.governing.maximumVonMises,profile.stressDisplayPrecision),
    stressLine('Maximum principal stress',review.rawStressReview.governing.maximumPrincipal,profile.stressDisplayPrecision),
    stressLine('Minimum principal stress',review.rawStressReview.governing.minimumPrincipal,profile.stressDisplayPrecision),'',
    '## Projected Stress','',
    `- Status: ${md(review.projectedStressReview.status)}`,
    `- Authority: ${md(review.projectedStressReview.authority || 'NOT_SUPPLIED')}`,
    `- Warning: ${md(review.projectedStressReview.authorityWarning || 'Projected stress is not available.')}`,'',
    '## Convergence Evidence','',
    `- Status: ${md(review.convergenceReview.status)}`,
    `- Warning: ${md(review.convergenceReview.warning)}`,
    ...(review.convergenceReview.status==='AVAILABLE'?[`- Study identity: ${md(review.convergenceReview.studyIdentity)}`,`- Quantities: ${review.convergenceReview.quantities.length}`]:[]),'',
    '## Diagnostics','',
    ...diagnosticLines(review.diagnostics),'',
    '## Limitations','',
    ...review.limitations.map((row)=>`- ${md(row)}`),'',
    '## Evidence Manifest','',
    ...(manifestRows.length?manifestRows.map((row)=>`- ${md(row.path)} — ${row.byteLength} bytes — ${md(row.contentHash || 'SELF_HASH_EXCLUDED')}`):['- Manifest is generated as part of the evidence-export package.']),'',
  ];
  return lines.join('\n').replace(/\n{3,}/g,'\n\n').trimEnd()+'\n';
}

function governingLine(label,row,precision){return row?`- ${label}: ${format(row.value,precision)} at node ${md(row.nodeId)}`:`- ${label}: unavailable`;}
function stressLine(label,row,precision){return row?`- ${label}: ${format(row.value,precision)} at ${md(row.elementId)}:${md(row.resultLocationId)}`:`- ${label}: unavailable`;}
function diagnosticLines(rows){return rows.length?rows.map((row)=>`- ${md(row.severity)} ${md(row.code)} [${md(row.sourceArtifactIdentity)}]: ${md(row.message)}`):['- No diagnostics.'];}
function format(value,precision){if(value===null||value===undefined)return'not supplied';const rounded=Number(value.toFixed(precision));return rounded.toFixed(precision);}
function md(value){return String(value??'').replaceAll('`','\\`').replaceAll('\n',' ');}
